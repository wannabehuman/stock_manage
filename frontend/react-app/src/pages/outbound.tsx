        inboundId: item.stock_code || "", // stock_codeÎ•º inboundIdÎ°ú Îß§Ìïë
        stock_code: item.stock_code || "",
        inbound_date: item.inbound_date || "",
        inboundQuantity: 0, // ÏÑúÎ≤ÑÏóêÏÑú Ï†úÍ≥µÌïòÏßÄ ÏïäÏúºÎØÄÎ°ú Í∏∞Î≥∏Í∞í
